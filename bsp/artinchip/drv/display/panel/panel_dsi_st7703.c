    panel_gpio_init(panel);

    panel_di_enable(panel, 0);
    panel_dsi_send_perpare(panel);

    panel_dsi_generic_send_seq(panel, 0xB9, 0xF1, 0x12, 0x83 );
    panel_dsi_generic_send_seq(panel, 0xB1, 0x00, 0x00, 0x00, 0xDA, 0x80 );
