    panel_gpio_init(panel);

    panel_di_enable(panel, 0);
    panel_dsi_send_perpare(panel);

    panel_dsi_generic_send_seq(panel, 0xB9,0xF1,0x12,0x87);
    panel_dsi_generic_send_seq(panel, 0xB2,0x40,0x08,0x78);
